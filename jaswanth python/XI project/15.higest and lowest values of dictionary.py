# to print the highest and lowest values in the dictionary

students = { 'ankit':89, 'parul':67,'paval':98,'rahul':85}
highest= max(students)
hmarks=max(students.values())
print('student with highest marks is  ',highest , ' scoring ',hmarks)
lowest=min(students)
mmarks=min(students.values())
print('student with lowest marks is  ',lowest,' scoring ',mmarks)
